import SwiftUI

extension UIColor {
    static var tealGreenDark: UIColor {
        .init(named: "tealGreenDark")!
    }
    static var tealGreen: UIColor {
        .init(named: "tealGreen")!
    }
    static var lime: UIColor {
        .init(named: "lime")!
    }
    static var lightGreen: UIColor {
        .init(named: "lightGreen")!
    }
    static var bone: UIColor {
        .init(named: "bone")!
    }
}

extension Color {
    static var tealGreenDark: Color {
        .init(uiColor: .tealGreenDark)
    }
    static var tealGreen: Color {
        .init(uiColor: .tealGreen)
    }
    static var lime: Color {
        .init(uiColor: .lime)
    }
    static var lightGreen: Color {
        .init(uiColor: .lightGreen)
    }
    static var bone: Color {
        .init(uiColor: .bone)
    }
}
